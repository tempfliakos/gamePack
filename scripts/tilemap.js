let tileH = 0;
let tileW = 0;
let mapW = 20;
let mapH = 20;
let image = new Image();
image.src = "../resources/tilemap_ground.png";

var currentSecond = 0, frameCount = 0, framesLastSecond = 0;

function drawMap(canvas, context) {
	tileW = canvas.width / mapW;
	tileH = canvas.height / mapH;

	var sec = Math.floor(Date.now()/1000);
	if(sec!=currentSecond)
	{
		currentSecond = sec;
		framesLastSecond = frameCount;
		frameCount = 1;
	}
	else { frameCount++; }

	for (var y = 0; y < mapH; ++y) {
		for (var x = 0; x < mapW; ++x) {
			switch (gameMap[((y * mapW) + x)]) {
				case 0:
					context.drawImage(image, 1 * 100 - 100, 0, 100, 100, x * tileW, y * tileH, tileW, tileH);
					break;
				default:
					context.drawImage(image, 1 * 100 - 100, 0, 100, 100, x * tileW, y * tileH, tileW, tileH);
			}
		}
	}

	context.font = "bold 15pt Arial";
	context.fillStyle = "#000000";
	context.fillText("FPS: " + framesLastSecond, 10, 30);
}